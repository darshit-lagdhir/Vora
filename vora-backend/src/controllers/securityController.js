/**
 * Triggers full-platform session revocation under Defcon Lockdown.
 * POST /api/v1/security/defcon
 */
export const triggerDefconLockdown = (req, res, next) => {
  try {
    const adminId = req.user.id;
    const adminEmail = req.user.email;

    // Hard-revoke sessions
    triggerGlobalLockdown(adminId);

    // Register in Immutable Audit Log
    const event = logSecurityEvent(
      'GLOBAL_SESSION_REVOCATION_EXECUTED',
      `Global Defcon lockdown initiated by administrator. All active JWT sessions revoked.`,
      {
        initiatedBy: adminId,
        email: adminEmail,
        lockdownTime: new Date().toISOString()
      },
      req
    );

    res.status(200).json({
      success: true,
      message: 'Global session revocation executed successfully.',
      data: {
        lockdownTime: globalLockdownTime,
        event
      }
    });
  } catch (err) {
    next(err);
  }
};


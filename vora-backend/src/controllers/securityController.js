/**
 * Helper to compute ETags and match client header requests (304 Not Modified).
 */
const handleETag = (req, res, data) => {
  const jsonString = JSON.stringify(data);
  const hash = crypto.createHash('sha1').update(jsonString).digest('base64');
  const etag = `W/"${hash}"`;

  res.setHeader('ETag', etag);

  if (req.headers['if-none-match'] === etag) {
    return res.status(304).end();
  }

  return res.status(200).json(data);
};

/**
 * Fetch all cryptographic and RBAC audit logs.
 * GET /api/v1/security/audit-logs
 */
export const getAuditLogs = (req, res, next) => {
  try {
    return handleETag(req, res, {
      success: true,
      data: auditLogs
    });
  } catch (err) {
    next(err);
  }
};


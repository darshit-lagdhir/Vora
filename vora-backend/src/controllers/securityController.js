/**
 * Simulates Master Key Pair generation for E2EE configuration using unmanaged native sandbox.
 * POST /api/v1/security/generate-key
 */
export const generateKeyPair = async (req, res, next) => {
  try {
    const adminId = req.user.id;
    const { algorithm = 'RSA-4096', keySize = 4096 } = req.body || {};
    
    // Invoke FFI key pair generation in sandboxed child process worker
    const keys = await nativeWorkerService.generateKeyPair(algorithm, keySize);
    
    // Compute key fingerprint hash from generated public key
    const publicKeyHash = crypto.createHash('sha256').update(keys.publicKey).digest('hex');
    const keyFingerprint = publicKeyHash.substring(0, 32).match(/.{2}/g).join(':');

    logSecurityEvent(
      'API_KEY_ROTATED',
      'Cryptographic master key pair regenerated by system administrator.',
      {
        actionInitiator: adminId,
        keyAlgorithm: algorithm,
        fingerprint: `sha256:${keyFingerprint}`
      },
      req
    );

    res.status(200).json({
      success: true,
      message: 'Cryptographic key pair successfully generated.',
      data: {
        publicKey: keys.publicKey,
        privateKey: keys.privateKey,
        fingerprint: `sha256:${keyFingerprint}`
      }
    });
  } catch (err) {
    next(err);
  }
};

/**
 * Verifies a cryptographic signature using FFI integration in isolated child process.
 * POST /api/v1/security/verify-signature
 */
export const verifySignature = async (req, res, next) => {
  try {
    const { signature, publicKey, msg } = req.body;
    
    const isValid = await nativeWorkerService.verifySignature(signature, publicKey, msg);

    logSecurityEvent(
      'SIGNATURE_VERIFIED',
      'Cryptographic signature verification processed via native sandboxed worker.',
      {
        result: isValid ? 'SUCCESS' : 'FAILED',
        signatureLength: signature ? signature.length : 0,
        publicKeyLength: publicKey ? publicKey.length : 0
      },
      req
    );

    res.status(200).json({
      success: true,
      message: isValid ? 'Signature is valid.' : 'Signature is invalid.',
      data: {
        isValid
      }
    });
  } catch (err) {
    next(err);
  }
};
